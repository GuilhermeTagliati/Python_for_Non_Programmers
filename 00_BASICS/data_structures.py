# STRING
x = "Hello World"
print(x)

# INT
x = 20
print(x)

# FLOAT
x = 20.5
print(x)

# COMPLEX
x = 1j
print(x)

#LIST
x = ["apple", "banana", "cherry"]
print(x)

# TUPLE
x = ("apple", "banana", "cherry")
print(x)

# RANGE
x = range(6)
print(x)

# DICT
x = {"name" : "John", "age" : 36}
print(x)

# SET
x = {"apple", "banana", "cherry"}
print(x)

# FROZEN SET
x = frozenset({"apple", "banana", "cherry"})
print(x)

# BOOL
x = True
print(x)

# BYTES
x = b"Hello"
print(x)

# BYTE ARRAY
x = bytearray(5)
print(x)

# MEMORY VIEW
x = memoryview(bytes(5))
print(x)

# NONE
x = None
print(x)
